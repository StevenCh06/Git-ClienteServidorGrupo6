    public void mostrarDatos(int cedula){
        
        for (int i=0;i<arrayProfesores.getProfesor().size();i++)
        {
            Profesor profesor = arrayProfesores.getProfesor().get(i);
            if (profesor.getCedula() == cedula) {
                txtNombre.setText(profesor.getNombre());
                txtApellido1.setText(profesor.getApellido1());
                txtApellido2.setText(profesor.getApellido2());
                txtCorreo.setText(profesor.getCorreo());
                txtSalario.setText(String.valueOf(profesor.getSalario()));
                
                if (profesor.isEstado()==true) {
                    comboEstado.setSelectedItem("Activo");
                }
                if (profesor.isEstado()==false) {
                    comboEstado.setSelectedItem("Inactivo");
                }
            }
            else{
            JOptionPane.showMessageDialog(null, "NO existe", "ERROR DE BUSQUEDA",JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    public void editar(){
        
        comboEstado.setEnabled(true);
        String nuevNombre = JOptionPane.showInputDialog(null,"Ingrese el Nuevo Nombre");
        String nuevApe1 = JOptionPane.showInputDialog(null,"Ingrese el Nuevo Primer Apellido");
        String nuevApe2 = JOptionPane.showInputDialog(null,"Ingrese el Nuevo Segundo Apellido");
        String nuevCorreo = JOptionPane.showInputDialog(null,"Ingrese el Nuevo Correo");
        double nuevSalario = Double.parseDouble(JOptionPane.showInputDialog(null,"Ingrese el Nuevo Salario"));
        
        int input = JOptionPane.showConfirmDialog(null, comboEstado,"'Â¿Cual es el estado del profesor?",JOptionPane.DEFAULT_OPTION);
        String nuevestado = comboEstado.getSelectedItem().toString();
        
        boolean estado = true;
        
        if(nuevestado.equals("Activo")){
            estado = true;
        }
        if(nuevestado.equals("Inactivo")){
            estado = false;
        }
        
        for (int i=0;i<arrayProfesores.getProfesor().size();i++)
        {
            Profesor profesor = arrayProfesores.getProfesor().get(i);
            arrayProfesores.getProfesor().get(i).setNombre(nuevNombre);
            arrayProfesores.getProfesor().get(i).setApellido1(nuevApe1);
            arrayProfesores.getProfesor().get(i).setApellido2(nuevApe2);
            arrayProfesores.getProfesor().get(i).setCorreo(nuevCorreo);
            arrayProfesores.getProfesor().get(i).setSalario(nuevSalario);
            arrayProfesores.getProfesor().get(i).setEstado(estado);
        }
    }
