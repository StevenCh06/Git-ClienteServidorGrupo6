    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        // Registrar
        int cedula=Integer.parseInt(txtCedula.getText());
        String nombre=txtNombre.getText();
        String apellido1=txtApellido1.getText();
        String apellido2=txtApellido2.getText();
        String correo=txtCorreo.getText();
        double salario=Double.parseDouble(txtSalario.getText());
    
        boolean estado = true;
        String tipo = comboEstado.getSelectedItem().toString();

        if(tipo.equals("Activo")){
            estado = true;
        }
        if(tipo.equals("Inactivo")){
            estado = false;
        }

        Profesor profesor = new Profesor(cedula, nombre, apellido1, apellido2, correo, salario, estado);
        arrayProfesores.getProfesor().add(profesor);

        JOptionPane.showMessageDialog(null, "Profesor agregado con exito");
        txtCedula.setText("");
        LimpiarCampos();
        BloquearCampos();

    }//GEN-LAST:event_agregarActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed

        int cedula=Integer.parseInt(txtCedula.getText());

        for (int i=0;i<arrayProfesores.getProfesor().size();i++)
        {
            Profesor xprofesor = arrayProfesores.getProfesor().get(i);
            if (xprofesor.getCedula() == cedula) {
                JOptionPane.showMessageDialog(null, "El Profesor ya existe","ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
                mostrarDatos(cedula);
                BloquearCampos();
                int resp = JOptionPane.showConfirmDialog(null, "Â¿Desea Editar Datos de Este Profesor?", "EDITAR", JOptionPane.YES_NO_OPTION);
                if(resp==JOptionPane.YES_OPTION){
                    editar();
                    mostrarDatos(cedula);
                    JOptionPane.showMessageDialog(null, "Datos Editados con Exito");
                }
                if(resp==JOptionPane.NO_OPTION){
                    txtCedula.setText("");
                    LimpiarCampos();
                }
            }
            else{
                DESBloquearCampos();
                JOptionPane.showMessageDialog(null, "El profesor NO existe puede agregarlo","VALIDANDO DATOS",JOptionPane.INFORMATION_MESSAGE);
                LimpiarCampos();
            }
        }
    }//GEN-LAST:event_buscarActionPerformed

